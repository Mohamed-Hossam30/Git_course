    private void B1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B1ActionPerformed
        // TODO add your handling code here:
                                     
   
        int supplier_id;
        try {
        supplier_id = Integer.parseInt(T1.getText().trim());
        boolean success = false;
        String supplier_name = T2.getText().trim();
        String contactInfo = T3.getText().trim();
        String productName = T4.getText().trim();

        
        if (supplier_name.isEmpty() || contactInfo.isEmpty() || productName.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter full details.");
            return;
        }

        
        
        try {
            success = Connection_to_sql.addProductToSupplier(supplier_id, supplier_name, productName, contactInfo);
        } 
        catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
            return;
        }
        
        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Product added/updated successfully!");
            T1.setText("");
            T2.setText("");
            T3.setText("");
            T4.setText("");
        }
        else {
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to add/update product.");
        }
            
        
        }
        catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Supplier ID must be a number.");
            return;
        }
    }//GEN-LAST:event_B1ActionPerformed

