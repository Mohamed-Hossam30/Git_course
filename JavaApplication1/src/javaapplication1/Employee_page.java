    private void B1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B1ActionPerformed
        // TODO add your handling code here:
      try {
        String input = T1.getText().trim();
        String[] parts = input.split(",");

        if (parts.length != 6) {
            JOptionPane.showMessageDialog(this, "Please enter 6 comma-separated values.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Clean inputs before parsing
        String part0 = Connection_to_sql.cleanInput(parts[0]);
        String part1 = Connection_to_sql.cleanInput(parts[1]);
        String part2 = Connection_to_sql.cleanInput(parts[2]);
        String part3 = Connection_to_sql.cleanInput(parts[3]);
        String part4 = Connection_to_sql.cleanInput(parts[4]);
        String part5 = Connection_to_sql.cleanInput(parts[5]);

        // Now parse
        String attendanceTime = String.valueOf(part0);
        String employeeId = String.valueOf(part1);
        String attendanceDate = String.valueOf(part2);       
        String checkInTime = String.valueOf(part3);        
        String checkOutTime = String.valueOf(part4);        
        String employeeName = String.valueOf(part5);

        boolean success = Connection_to_sql.addAttendanceRecordFromStrings(attendanceTime, employeeId, attendanceDate, checkInTime, checkOutTime, employeeName);

        if (success) {
            JOptionPane.showMessageDialog(this, "Attendance registered successfully.");
        }
        else {
            JOptionPane.showMessageDialog(this, "Failed to register attendance.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Invalid input. Please check your data format.", "Exception", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_B1ActionPerformed

